 const addition = (x, y) => {
    return x + y
};

 const subtraction = (x, y) => {
    return x - y
};

 const multiplication = (x, y) => {
    return x * y
};

 const division = (x, y) => {
    return x / y
};

 const modulo = (x, y) => {
    return x % y
};


module.exports = {
    addition,
    subtraction,
    multiplication,
    division,
    modulo
};
